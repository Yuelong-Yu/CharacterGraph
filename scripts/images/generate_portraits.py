@retry(
    stop=stop_after_attempt(3),
    wait=wait_exponential(multiplier=3, min=3, max=30),
    retry=retry_if_exception_type(Exception),
)
def generate_one(slug: str, character_desc: str) -> bytes:
    """单图生成 + 下载，返回 PNG 字节。"""
    full_prompt = f"{BASE_STYLE_ZH}。\n\n{character_desc}。"
    resp = client.images.generate(
        model=MODEL,
        prompt=full_prompt,
        size="2K",
        output_format="png",
        response_format="url",
        watermark=False,
    )
    if not resp.data:
        raise RuntimeError(f"{slug}: no data returned")
    url = resp.data[0].url
    r = requests.get(url, timeout=60)
    r.raise_for_status()
    return r.content


def process_to_portrait_and_thumb(raw_png: bytes, slug: str) -> None:
    """
    把 doubao 返回的大图（约 1664×2496 PNG）处理为：
      - portraits/{slug}.webp  — 800×1200, 质量 88
      - thumbs/{slug}.webp     — 128×192 (保持 2:3), 质量 85
    """
    # 落原图（.gitignore，便于回查）
    raw_path = RAW_DIR / f"{slug}.png"
    raw_path.write_bytes(raw_png)

    from io import BytesIO
    img = Image.open(BytesIO(raw_png)).convert("RGB")

    # 等比缩放到 800 宽（保留 2:3），如果 doubao 返回不是 2:3 则居中裁剪
    target_w, target_h = 800, 1200
    src_w, src_h = img.size
    src_ratio = src_w / src_h
    target_ratio = target_w / target_h

    if abs(src_ratio - target_ratio) > 0.02:
        # 居中裁剪到 2:3
        if src_ratio > target_ratio:
            new_w = int(src_h * target_ratio)
            left = (src_w - new_w) // 2
            img = img.crop((left, 0, left + new_w, src_h))
        else:
            new_h = int(src_w / target_ratio)
            top = (src_h - new_h) // 4  # 偏上，保留头部
            img = img.crop((0, top, src_w, top + new_h))

    portrait = img.resize((target_w, target_h), Image.LANCZOS)
    portrait.save(PORTRAITS_DIR / f"{slug}.webp", "WEBP", quality=88, method=6)

    # 缩略：保留 2:3，128×192
    thumb = portrait.resize((128, 192), Image.LANCZOS)
    thumb.save(THUMBS_DIR / f"{slug}.webp", "WEBP", quality=85, method=6)


def main(target_slugs: list[str] | None = None):
    if target_slugs is None:
        target_slugs = list(CHARACTER_PROMPTS.keys())

    print(f"准备生成 {len(target_slugs)} 张：{target_slugs}")
    print()

    ok = 0
    fail: list[str] = []
    for i, slug in enumerate(target_slugs, start=1):
        if slug not in CHARACTER_PROMPTS:
            print(f"[{i}/{len(target_slugs)}] ✗ {slug}: 无 prompt 定义")
            fail.append(slug)
            continue

        portrait_path = PORTRAITS_DIR / f"{slug}.webp"
        if portrait_path.exists():
            print(f"[{i}/{len(target_slugs)}] {slug}: 已存在，跳过")
            ok += 1
            continue

        desc = CHARACTER_PROMPTS[slug]
        print(f"[{i}/{len(target_slugs)}] {slug}: 生成中...")
        t0 = time.time()
        try:
            raw = generate_one(slug, desc)
        except Exception as e:
            print(f"  ✗ {type(e).__name__}: {e}")
            fail.append(slug)
            continue

        process_to_portrait_and_thumb(raw, slug)
        dt = time.time() - t0
        print(f"  ✓ {dt:.1f}s, raw {len(raw)//1024}KB → portraits/{slug}.webp + thumbs/{slug}.webp")
        ok += 1

    print()
    print("=" * 60)
    print(f"完成 {ok}/{len(target_slugs)}")
    if fail:
        print(f"失败：{fail}")


if __name__ == "__main__":
    args = sys.argv[1:]
    main(args if args else None)
