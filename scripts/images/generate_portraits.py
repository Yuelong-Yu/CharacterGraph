@retry(
    stop=stop_after_attempt(3),
    wait=wait_exponential(multiplier=3, min=3, max=30),
    retry=retry_if_exception_type(Exception),
)
def generate_one(slug: str, desc: str, base_style: str = BASE_STYLE_ZH) -> bytes:
    """单图生成 + 下载,返回 PNG 字节。base_style 可切换人物/器物。"""
    full_prompt = f"{base_style}。\n\n{desc}。"
    resp = client.images.generate(
        model=MODEL,
        prompt=full_prompt,
        size="2K",
        output_format="png",
        response_format="url",
        watermark=False,
    )
    if not resp.data:
        raise RuntimeError(f"{slug}: no data returned")
    url = resp.data[0].url
    r = requests.get(url, timeout=60)
    r.raise_for_status()
    return r.content


def process_to_portrait_and_thumb(raw_png: bytes, slug: str) -> None:
    """
    把 doubao 返回的大图（约 1664×2496 PNG）处理为：
      - portraits/{slug}.webp  — 800×1200, 质量 88
      - thumbs/{slug}.webp     — 128×192 (保持 2:3), 质量 85
    """
    # 落原图（.gitignore，便于回查）
    raw_path = RAW_DIR / f"{slug}.png"
    raw_path.write_bytes(raw_png)

    from io import BytesIO
    img = Image.open(BytesIO(raw_png)).convert("RGB")

    # 等比缩放到 800 宽（保留 2:3），如果 doubao 返回不是 2:3 则居中裁剪
    target_w, target_h = 800, 1200
    src_w, src_h = img.size
    src_ratio = src_w / src_h
    target_ratio = target_w / target_h

    if abs(src_ratio - target_ratio) > 0.02:
        # 居中裁剪到 2:3
        if src_ratio > target_ratio:
            new_w = int(src_h * target_ratio)
            left = (src_w - new_w) // 2
            img = img.crop((left, 0, left + new_w, src_h))
        else:
            new_h = int(src_w / target_ratio)
            top = (src_h - new_h) // 4  # 偏上，保留头部
            img = img.crop((0, top, src_w, top + new_h))

    portrait = img.resize((target_w, target_h), Image.LANCZOS)
    portrait.save(PORTRAITS_DIR / f"{slug}.webp", "WEBP", quality=88, method=6)

    # 缩略：保留 2:3，128×192
    thumb = portrait.resize((128, 192), Image.LANCZOS)
    thumb.save(THUMBS_DIR / f"{slug}.webp", "WEBP", quality=85, method=6)


def _generate_and_save(slug: str) -> tuple[str, str | None]:
    """Worker:返回 (slug, error_msg or None)。供线程池并发调用。
    自动从 CHARACTER_PROMPTS / ARTIFACT_PROMPTS 中查找,使用对应的 base style。"""
    if slug in CHARACTER_PROMPTS:
        desc = CHARACTER_PROMPTS[slug]
        base_style = BASE_STYLE_ZH
    elif slug in ARTIFACT_PROMPTS:
        desc = ARTIFACT_PROMPTS[slug]
        base_style = BASE_STYLE_ARTIFACT_ZH
    else:
        return slug, "无 prompt 定义"

    portrait_path = PORTRAITS_DIR / f"{slug}.webp"
    if portrait_path.exists():
        print(f"  {slug}: 已存在，跳过", flush=True)
        return slug, None

    print(f"  {slug}: 生成中...", flush=True)
    t0 = time.time()
    try:
        raw = generate_one(slug, desc, base_style=base_style)
        process_to_portrait_and_thumb(raw, slug)
    except Exception as e:
        return slug, f"{type(e).__name__}: {e}"
    dt = time.time() - t0
    print(f"  ✓ {slug}: {dt:.1f}s, raw {len(raw)//1024}KB → portraits/{slug}.webp + thumbs/{slug}.webp", flush=True)
    return slug, None


def main(target_slugs: list[str] | None = None, parallel: int = 5):
    if target_slugs is None:
        # 默认生成全部 — Character + Artifact
        target_slugs = list(CHARACTER_PROMPTS.keys()) + list(ARTIFACT_PROMPTS.keys())

    print(f"准备生成 {len(target_slugs)} 张，并行 {parallel}：{target_slugs}")
    print()

    ok = 0
    fail: list[tuple[str, str]] = []
    with ThreadPoolExecutor(max_workers=parallel) as ex:
        future_map = {ex.submit(_generate_and_save, slug): slug for slug in target_slugs}
        for fut in as_completed(future_map):
            slug, err = fut.result()
            if err:
                fail.append((slug, err))
            else:
                ok += 1

    print()
    print("=" * 60)
    print(f"完成 {ok}/{len(target_slugs)}")
    if fail:
        print(f"失败：")
        for slug, err in fail:
            print(f"  - {slug}: {err}")


if __name__ == "__main__":
    args = sys.argv[1:]
    # 支持 `--parallel N` 选项
    parallel = 5
    slugs: list[str] = []
    i = 0
    while i < len(args):
        if args[i] == "--parallel" and i + 1 < len(args):
            parallel = int(args[i + 1])
            i += 2
        else:
            slugs.append(args[i])
            i += 1
    main(slugs if slugs else None, parallel=parallel)
